// Javascript Bitwise Operators
// & AND: Sets each bit to 1 if both bits are 1
// | OR: Sets each bit to 1 if one of the two bits is 1
// ^ XOR: Sets each bit to 1 if only one of the two bits is 1
// ~ NOT: Inverts all the bits
// << Zero fill left shift: Shifts left by pushing  zeros in from the right and let the leftmost bits fall of
// >> Signed right shift: Shifts right by pushing copies of the leftmost bit in from the left, and let the rightmost bits fall off
// >>> Zero fill right shift: Shifts right by pushing  zeros in from the left and let the rightmost bits fall of

console.log(5 & 1);
console.log(0101 & 0001);